// Array

// O Typescript, assim como o Javascript, permite a você trabalhar com arrays
// de valores. Tipos Array podem ser escritos de duas formas. Na primeira, você
// usa o tipo dos elementos seguido de [] para denotar um array do tipo do
// elemento.
let list: number[] = [1, 2, 3];

// Na segunda forma, você usa um tipo genérico de Array, Array<elemType>:
let list2: Array<number> = [1, 2, 3];
